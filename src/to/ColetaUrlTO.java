package to;

public class ColetaUrlTO {
	
	// Atríbutos
	private int id;
	private String url;
	
	// Métodos Modificadores
	public void setId(int id) {
		this.id = id;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	// Métodos de Acesso
	public int getId() {
		return id;
	}
	
	public String getUrl() {
		return url;
	}
}
